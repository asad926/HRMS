package com.developer101.mavenproject2.model;

public class Countries {
    private String cun_id;
    private String cun_name;
    private String regin_id;

    public String getCun_id() {
        return cun_id;
    }

    public void setCun_id(String cun_id) {
        this.cun_id = cun_id;
    }

    public String getCun_name() {
        return cun_name;
    }

    public void setCun_name(String cun_name) {
        this.cun_name = cun_name;
    }

    public String getRegin_id() {
        return regin_id;
    }

    public void setRegin_id(String regin_id) {
        this.regin_id = regin_id;
    }
}
